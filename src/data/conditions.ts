export function searchConditions(query: string): Condition[] {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase();
  return CONDITIONS.filter(c =>
    c.name.toLowerCase().includes(q) ||
    c.category.toLowerCase().includes(q)
  ).slice(0, 8);
}

